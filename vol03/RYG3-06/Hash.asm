        stx glTempX

        lda #0
        sta glLock

        ldx #0

glAgain

        lda board,x

glMouse

        cmp #PIECE_MOUSE
        bne glCat1

        lda lock_mouse,x

        eor glLock
        sta glLock

        jmp glNext
        
glCat1

        cmp #PIECE_CAT1
        bne glCat2

        lda lock_cat1,x

        eor glLock
        sta glLock

        jmp glNext

glCat2

        cmp #PIECE_CAT2
        bne glCat3

        lda lock_cat2,x

        eor glLock
        sta glLock

        jmp glNext

glCat3

        cmp #PIECE_CAT3
        bne glCat4

        lda lock_cat3,x

        eor glLock
        sta glLock

        jmp glNext

glCat4

        cmp #PIECE_CAT4
        bne glNext

        lda lock_cat4,x

        eor glLock
        sta glLock

glNext

        inx
        cpx #64
        bne glAgain

        ldx glTempX

        rts

; ----------------------------------------

lumTempX        byte $00

lookUpMouse

        stx lumTempX

        ldx current_hash

        lda hashpos_mouse_start,x
        cmp #$ff
        beq lumFalse

        lda hashpos_mouse_lock,x
        cmp current_lock
        bne lumFalse

lumTrue

        lda hashpos_mouse_start,x
        sta hash_start

        lda hashpos_mouse_dest,x
        sta hash_dest

        ldx lumTempX

        rts

lumFalse

        lda #$ff
        sta hash_start
        sta hash_dest

        ldx lumTempX

        rts

; ----------------------------------------

